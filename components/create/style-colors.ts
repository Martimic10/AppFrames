export type GradientStyle = {
  from: string;
  via: string;
  to: string;
  angle: number;
};

export const DEFAULT_GRADIENT: GradientStyle = {
  from: "#27272a",
  via: "#18181b",
  to: "#09090b",
  angle: 160
};

export function gradientToCss(gradient: GradientStyle): string {
  return `linear-gradient(${gradient.angle}deg, ${gradient.from} 0%, ${gradient.via} 50%, ${gradient.to} 100%)`;
}

